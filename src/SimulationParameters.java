public class SimulationParameters {
    private float growthRate;
    private float motilityRate;
    private float EPSProductionRate;
    private float PslProductionRate;

    public void setGrowthRate(float rate) {
        this.growthRate = rate;
    }

    public void setMotilityRate(float rate) {
        this.motilityRate = rate;
    }

    public void setEPSProductionRate(float rate) {
        this.EPSProductionRate = rate;
    }

    public void setPslProductionRate(float rate) {
        this.PslProductionRate = rate;
    }
}
